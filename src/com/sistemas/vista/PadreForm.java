public final void cargarTabla(){
    limpiarTabla();
    model = (DefaultTableModel) tblista.getModel();
    lista2 = aO1.listarPadre();
    Object []datos = new Object[4];
    for(int i=0;i<lista2.size();i++){
        datos[0]=lista2.get(i).getIdpadre();
        datos[1]=lista2.get(i).getNombres();
        datos[2]=lista2.get(i).getApellidos();
        datos[3]=lista2.get(i).getNcelular();
        
     model.addRow(datos);
     
     tblista.setModel(model);
    }
}
