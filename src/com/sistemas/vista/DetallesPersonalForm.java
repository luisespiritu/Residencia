    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        
        if(tblista.getSelectedRow()>=0){
            int fila = tblista.getSelectedRow();
            int id = (int) tblista.getValueAt(fila, 1);
             int op = adp.eliminarDetalle(id);
            if(op==1){
                JOptionPane.showMessageDialog(null, "Registro Eliminado");
                LimpiarJTable();
                cargarTabla();
            }else{
                JOptionPane.showMessageDialog(null, "Registro no Eliminado");
            }
        }else{
        JOptionPane.showMessageDialog(null, "Seleccionar Registro");
        }
        
        
        
        
    }//GEN-LAST:event_btneliminarActionPerformed
void cargarTabla(){
    
    lista = adp.listarDetalle();
    model = (DefaultTableModel) tblista.getModel();
    Object[] datos = new Object[4];
    for(int i=0;i<lista.size();i++){
        datos[0]=i+1;
        datos[1]=lista.get(i).getIddetallepersonal();
        datos[2]=lista.get(i).getCantipagada();
        datos[3]=lista.get(i).getDescuento();
        
     model.addRow(datos);
    }
     tblista.setModel(model);    
}
 void limpiarComponentes(){
   
    txtcantidad1.setText(null);
    txtdescuento.setText(null);
    txtiddetalle.setText(null);
      
    
 }
     void LimpiarJTable(){
        int a = model.getRowCount()-1;  //Ãndices van de 0 a n-1
         for(int i=a;i>=0;i--){  
            model.removeRow(i);
        }
    }

